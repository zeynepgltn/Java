package com.zeynepgulten._1_part_javatypes;

public class _02_JavaPrimitiveTypes {
    public static void main(String[] args) {
        // Primitive Types: 8 tanedir. null değer alamazsınız.
        // tam sayılar (4)
        byte b1 = 127;
        short s1 = 32767;
        int i1 = 2147483647;
        long l1 = 9223372036854775807L;

        // virgüllü (floating point)
        float f2 = 3.14f;
        double d2 = 3.14;

        // boolean
        boolean b3 = true;

        // char
        char c4 = '\u4125';

        // Veri değişken isimlerini yazarken;

        /*
        veri isimlendirmeleri;
        1-) İsim, sıfat veya zamir kullanmamız lazım fiil olmaz.
        2-) _ veya $ dışında özel simgelerle başlanmaz.
        3-) Sayı ile bitebilir ama başlanmaz.
        4-) camelCase kullanılır.
        Tanımlanan değişken tekrar tanımlanamaz.
        */
        int schoolNumber = 34;

    }
}
